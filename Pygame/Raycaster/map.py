	def is_collide(self , x , y , pixel = True):
		if pixel:
			return self.map_grid[int(y // tile_height)][int(x // tile_width)]
		else:
			return self.map_grid[y][x]
		
	def draw(self , surface = screen):
		for row in range(map_size):
			for col in range(map_size):
				cell = self.map_grid[col][row]
				self.color = wall_color if cell else ground_color
				pygame.draw.rect(surface , self.color , (row * tile_width , col * tile_height , tile_width , tile_height))